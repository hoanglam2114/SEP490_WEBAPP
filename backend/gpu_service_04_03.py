def load_model():
    global _current_infer_model, _current_infer_tokenizer, _current_infer_model_id

    data = request.json
    hf_model_id = data.get('hf_model_id')

    if not hf_model_id:
        return jsonify({"error": "Missing hf_model_id"}), 400

    try:
        # Kiểm tra xem mô hình đã được nạp sẵn chưa
        if (_current_infer_model is None
                or _current_infer_tokenizer is None
                or _current_infer_model_id != hf_model_id):

            _release_gpu_memory()
            print(f"Loading model {hf_model_id} into GPU...")

            _current_infer_model, _current_infer_tokenizer = FastLanguageModel.from_pretrained(
                model_name=hf_model_id,
                max_seq_length=2048,
                load_in_4bit=True,
            )

            if getattr(_current_infer_tokenizer, "pad_token", None) is None:
                _current_infer_tokenizer.pad_token = _current_infer_tokenizer.eos_token
            _current_infer_tokenizer.padding_side = "right"

            _current_infer_model_id = hf_model_id
            return jsonify({"status": "success", "message": f"Model {hf_model_id} loaded successfully."}), 200
        else:
            print(f"Model {hf_model_id} is already loaded.")
            return jsonify({"status": "success", "message": f"Model {hf_model_id} is already loaded."}), 200

    except Exception as e:
        _release_gpu_memory()
        traceback.print_exc()
        return jsonify({"error": str(e)}), 500

