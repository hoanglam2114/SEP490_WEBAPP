# ─────────────────────────────────────────────
# Filter <think>...</think> khi stream
# ─────────────────────────────────────────────
def stream_without_thinking(streamer):
    """
    Accumulate buffer liên tục để tránh <think>/<think> bị split
    giữa 2 chunk (TextIteratorStreamer trả về từng token một).

    Logic:
    - Chưa gặp <think>  → yield bình thường (non-thinking model)
    - Đang trong <think> → bỏ hết, chỉ giữ 9 ký tự cuối phòng tag bị cắt
    - Sau </think>       → yield tất cả như bình thường
    """
    OPEN_TAG  = "<think>"
    CLOSE_TAG = "</think>"

    buffer        = ""
    in_think      = False
    thinking_done = False

    for chunk in streamer:
        buffer += chunk

        # Chưa vào think block
        if not in_think and not thinking_done:
            if OPEN_TAG in buffer:
                in_think = True
                buffer = buffer.split(OPEN_TAG, 1)[1]
            else:
                # Yield phần an toàn, giữ 10 ký tự cuối phòng tag đang đến dở
                if len(buffer) > 10:
                    yield buffer[:-10]
                    buffer = buffer[-10:]
                continue

        # Đang trong think block
        if in_think:
            if CLOSE_TAG in buffer:
                in_think      = False
                thinking_done = True
                buffer = buffer.split(CLOSE_TAG, 1)[1]
                # Không yield phần think, tiếp tục xử lý phần sau tag
            else:
                # Vẫn trong think — bỏ, giữ 9 ký tự cuối phòng </think> bị cắt
                buffer = buffer[-9:] if len(buffer) > 9 else buffer
                continue

        # Đã qua think block — yield bình thường
        if thinking_done and buffer:
            yield buffer
            buffer = ""

    # Flush cuối
    if buffer and not in_think:
        yield buffer


