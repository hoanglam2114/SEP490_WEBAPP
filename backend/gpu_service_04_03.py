@app.route('/api/infer/stream', methods=['POST'])
def infer_model_stream():
    global _current_infer_model, _current_infer_tokenizer, _current_infer_model_id, inference_logs_db


    # RÀNG BUỘC: Kiểm tra xem model đã được nạp trước đó chưa
    if _current_infer_model is None or _current_infer_tokenizer is None:
        return jsonify({"error": "No model loaded. Please call /api/model/load first."}), 400

    data          = request.json
    text_input    = data.get('text_input')
    system_prompt = data.get('system_prompt', DEFAULT_SYSTEM_PROMPT)

    # RAG: augment system_prompt với web search nếu câu hỏi cần thông tin thời sự
    system_prompt, _searched = build_rag_system_prompt(system_prompt, text_input or '')
    if _searched:
        print("[RAG] Đã augment system prompt với web search context.")

    hf_model_id   = data.get('hf_model_id') # Nhận vào để verify chéo (tùy chọn)

    # Kiểm tra chéo xem user có đang request đúng model đang chạy trên GPU không
    if hf_model_id and hf_model_id != _current_infer_model_id:
        return jsonify({"error": f"Model mismatch. Requested {hf_model_id} but {_current_infer_model_id} is loaded."}), 400

    if not text_input:
        return jsonify({"error": "Missing text_input"}), 400

    # Parse tham số thế hệ
    gen_max_new_tokens     = data.get('max_new_tokens', 512)
    gen_temperature        = data.get('temperature', 0.3)
    gen_top_k              = data.get('top_k', 40)
    gen_top_p              = data.get('top_p', 0.85)
    gen_repetition_penalty = data.get('repetition_penalty', 1.15)
    gen_do_sample          = data.get('do_sample', True)

    print("System prompt",system_prompt)
    print("Max new token:",gen_max_new_tokens)
    print("Temperature:",gen_temperature)
    print("Top k:",gen_top_k)
    print("Top p:",gen_top_p)
    print("Repetition penalty:",gen_repetition_penalty)

    inference_id = str(uuid.uuid4())
    inference_log = {
        "inference_id": inference_id,
        "timestamp": datetime.datetime.now().isoformat(),
        "model_id": _current_infer_model_id,
        "input_parameters": {
            "text_input": text_input,
            "system_prompt": system_prompt,
            "max_new_tokens": gen_max_new_tokens,
            "temperature": gen_temperature,
            "top_k": gen_top_k,
            "top_p": gen_top_p,
            "repetition_penalty": gen_repetition_penalty,
            "do_sample": gen_do_sample,
        },
        "generated_text": "",
        "status": "started"
    }
    inference_logs_db[inference_id] = inference_log

    try:
        instruction_text = format_inference_prompt(
            _current_infer_tokenizer,
            system_prompt,
            text_input,
        )

        inputs = _current_infer_tokenizer(
            text=instruction_text,
            return_tensors="pt"
        ).to("cuda")

        streamer = TextIteratorStreamer(
            _current_infer_tokenizer,
            skip_prompt=True,
            skip_special_tokens=True,
        )

        generation_kwargs = dict(
            **inputs,
            streamer=streamer,
            max_new_tokens=gen_max_new_tokens,
            do_sample=gen_do_sample,
            temperature=gen_temperature,
            top_k=gen_top_k,
            top_p=gen_top_p,
            repetition_penalty=gen_repetition_penalty,
            eos_token_id=_current_infer_tokenizer.eos_token_id,
        )

        Thread(
            target=_current_infer_model.generate,
            kwargs=generation_kwargs,
            daemon=True,
        ).start()

        def generate_stream():
            full_response = []
            for text in stream_without_thinking(streamer):
                full_response.append(text)
                yield f"data: {json.dumps({'text': text}, ensure_ascii=False)}\n\n"
            inference_logs_db[inference_id]["generated_text"] = "".join(full_response)
            inference_logs_db[inference_id]["status"] = "completed"
            yield "data: [DONE]\n\n"

        return Response(generate_stream(), mimetype='text/event-stream')

    except Exception as e:
        traceback.print_exc()
        inference_logs_db[inference_id]["status"] = "error"
        inference_logs_db[inference_id]["error_message"] = str(e)
        return jsonify({"error": str(e)}), 500

