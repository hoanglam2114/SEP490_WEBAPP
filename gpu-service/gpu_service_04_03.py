def _release_slot(slot_id: int):
    slot = _model_slots.pop(slot_id, None)
    if slot is None:
        return
    del slot["model"]
    del slot["tokenizer"]
    gc.collect()
    torch.cuda.empty_cache()
    print(f"\n--- Slot {slot_id}: GPU memory released ---\n")

