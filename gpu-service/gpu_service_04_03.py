@app.route('/api/model/load', methods=['POST'])
def load_model():
    data = request.json
    print(f"[load_model] raw body keys: {list(data.keys())}")
    print(f"[load_model] instanceId field: {data.get('instanceId')!r}")

    hf_model_id = data.get('hf_model_id')
    instance_id = data.get('instance_id') or data.get('instanceId') or 1
    slot_id = int(instance_id)
    print(f"[load_model] → resolved slot_id={slot_id}, hf_model_id={hf_model_id!r}")

    if not hf_model_id:
        return jsonify({"error": "Missing hf_model_id"}), 400

    if slot_id not in _slot_locks:
        return jsonify({"error": f"instanceId phải là 1 hoặc 2, nhận: {slot_id}"}), 400

    lock = _slot_locks[slot_id]

    # Lock slot trước khi thao tác để tránh load song song vào cùng 1 slot
    if not lock.acquire(blocking=False):
        return jsonify({"error": f"Slot {slot_id} đang bận (đang infer/load). Thử lại sau."}), 409

    try:
        current = _model_slots.get(slot_id)
        if current and current["model_id"] == hf_model_id:
            print(f"[Slot {slot_id}] Model {hf_model_id} đã được load sẵn.")
            return jsonify({"status": "success",
                            "message": f"Model {hf_model_id} is already loaded in slot {slot_id}."}), 200

        # Giải phóng slot cũ trước khi load model mới
        if current:
            print(f"[Slot {slot_id}] Giải phóng model cũ: {current['model_id']}")
            del current["model"]
            del current["tokenizer"]
            del _model_slots[slot_id]
            gc.collect()
            torch.cuda.empty_cache()

        print(f"[Slot {slot_id}] Loading model {hf_model_id}...")
        model, tokenizer = FastLanguageModel.from_pretrained(
            model_name=hf_model_id,
            max_seq_length=2048,
            load_in_4bit=True,
        )

        if getattr(tokenizer, "pad_token", None) is None:
            tokenizer.pad_token = tokenizer.eos_token
        tokenizer.padding_side = "right"

        _model_slots[slot_id] = {
            "model":    model,
            "tokenizer": tokenizer,
            "model_id": hf_model_id,
        }
        print(f"[Slot {slot_id}] ✅ Model {hf_model_id} loaded successfully.")
        return jsonify({"status": "success",
                        "message": f"Model {hf_model_id} loaded into slot {slot_id}."}), 200

    except Exception as e:
        traceback.print_exc()
        return jsonify({"error": str(e)}), 500

    finally:
        lock.release()

