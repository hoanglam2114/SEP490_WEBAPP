def _build_conv_text(idx: int, system_prompt: str, turns: list) -> str:
    lines = [f"=== HỘI THOẠI {idx} ===",
             f"[SYSTEM PROMPT]\n{system_prompt}",
             "[HỘI THOẠI]"]
    for t in turns:
        lines.append(f"Học sinh: {t['user']}\nGia sư: {t['model_response']}")
    return "\n\n".join(lines)

