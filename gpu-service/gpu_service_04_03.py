# ── 1. Rewrite query thành search query tốt hơn ──────────────────────
def rewrite_query(user_text: str) -> str:
    """
    Chuyển câu hỏi tự nhiên → search query ngắn gọn, tiếng Việt, có năm.
    Ví dụ: "Giá xăng ở Việt Nam hôm nay thế nào?" → "giá xăng Việt Nam 2026"
    """
    today = datetime.date.today()
    year  = today.year
    text  = user_text.lower().strip()

    # Thay thế các cụm thời gian mơ hồ bằng thông tin cụ thể
    for phrase in ["hôm nay", "hiện tại", "hiện nay", "ngày hôm nay",
                   "bây giờ", "lúc này", "thời điểm này"]:
        text = text.replace(phrase, str(year))

    for phrase in ["thế nào", "ra sao", "như thế nào", "bao nhiêu",
                   "là gì", "ở đâu", "khi nào", "?", "nhỉ", "nhé", "ạ"]:
        text = text.replace(phrase, "")

    # Thêm "Việt Nam" nếu chưa có và câu có vẻ hỏi về VN
    vn_hints = ["xăng", "điện", "học phí", "tuyển sinh", "điểm chuẩn",
                "lương", "thuế", "nghị định", "thông tư", "lãi suất"]
    if any(h in text for h in vn_hints) and "việt nam" not in text:
        text = text + " Việt Nam"

    return text.strip()


# ── 2. Web search với query đã rewrite ───────────────────────────────
def web_search(query: str, max_results: int = 3) -> str:
    """Tìm DuckDuckGo, trả về context string. Trả về "" nếu lỗi."""
    try:
        with DDGS() as ddgs:
            results = list(ddgs.text(
                query,
                max_results=max_results,
                region="vn-vi",
            ))
        if not results:
            return ""
        parts = []
        for r in results:
            title   = r.get("title", "")
            snippet = r.get("body", "")
            parts.append(f"- {title}: {snippet}")
        return "\n".join(parts)
    except Exception as e:
        print(f"[Web Search Error] {e}")
        return ""


# ── 3. Phát hiện câu hỏi cần tra mạng ───────────────────────────────
def needs_web_search(text: str) -> bool:
    keywords = [
        "hôm nay", "hiện tại", "hiện nay", "mới nhất", "gần đây",
        "tuần này", "tháng này", "năm nay", "vừa", "ngày mai",
        "giá", "tỷ giá", "thời tiết", "tin tức", "kết quả", "lịch",
        "bao nhiêu tiền", "học phí", "tuyển sinh", "điểm chuẩn",
        "nghị định", "thông tư", "quy định mới", "lãi suất",
        "today", "current", "latest", "now",
    ]
    return any(kw in text.lower() for kw in keywords)


# ── 4. Build augmented system prompt — context nhét vào đúng chỗ ────
def build_rag_system_prompt(base_system_prompt: str, query: str) -> tuple:
    """
    Trả về (system_prompt_mới, đã_search).
    Context được đưa vào CUỐI system prompt, trước khi model nhận user message.
    """
    if not needs_web_search(query):
        return base_system_prompt, False

    search_query = rewrite_query(query)
    today_str    = datetime.date.today().strftime("%d/%m/%Y")

    print(f"[🔍 RAG] Query gốc : {query}")
    print(f"[🔍 RAG] Search query: {search_query}")

    context = web_search(search_query)

    if not context:
        print("[🔍 RAG] Không tìm được kết quả.")
        return base_system_prompt, False

    print(f"[🔍 RAG] OK — {len(context)} ký tự context")

    # Context đặt ở CUỐI system prompt, ngắn gọn, không dùng markdown phức tạp
    augmented = (
        f"{base_system_prompt}\n\n"
        f"[Thông tin tra cứu ngày {today_str}]\n"
        f"{context}\n"
        f"Dùng thông tin trên để trả lời nếu liên quan. "
        f"Nếu không chắc, nói rõ với học sinh."
    )
    return augmented, True


