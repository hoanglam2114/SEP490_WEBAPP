def formatting_prompts_func(examples,tokenizer):
    texts = []

    # Lặp qua từng example trong batch
    for messages in examples["messages"]:
        # Kiểm tra xem mẫu đã có System Prompt chưa
        has_system = any(msg.get("role") == "system" for msg in messages)

        # Nếu chưa có, chèn system prompt mặc định vào ĐẦU mảng
        if not has_system:
            messages.insert(0, {"role": "system", "content": DEFAULT_SOCRATIC_PROMPT})

        # SỬ DỤNG TOKENIZER ĐỂ ÁP DỤNG CHAT TEMPLATE CỦA MÔ HÌNH
        # tokenize=False để trả về dạng text thô thay vì dạng mảng ID số
        formatted_text = tokenizer.apply_chat_template(
            messages,
            tokenize=False,
            add_generation_prompt=False
        )

        texts.append(formatted_text)

    return {"text": texts}

